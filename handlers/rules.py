from aiogram import Router, F
from aiogram.filters import Command
from aiogram.types import Message

router = Router()

RULES_TEXT = """📖 <b>Правила турнира прогнозов на ЧМ-2026</b>

<b>Групповой этап</b>
За каждый матч группового этапа можно получить:
• 3 очка — точный счёт
• 1 очко — угадан исход (победа / ничья), но не счёт
• 0 очков — мимо

<b>Плей-офф</b>
Сначала выбираешь исход, затем вводишь счёт:
• «[Сборная] в осн. время» → финальный счёт
• «[Сборная] в доп. время» → счёт после 120 мин.
• «[Сборная] по пенальти» → счёт после 120 мин. (ничья)

За каждый матч плей-офф можно получить:
• 4 очка — угадан исход + точный счёт
• 3 очка — угадан исход: победа в доп. время или по пенальти (без точного счёта)
• 2 очка:
  – победитель в осн. вр. (без счёта)
  – победитель + верный счёт, но матч ушёл в доп. время или пенальти (при пен. верный счёт = счёт 120 мин. + решающий гол: ставка 1:2, матч 1:1 и победа по пен.)
  – победитель угадан, но доп. время ↔️ пенальти перепутаны
  – угадана серия пенальти, но не победитель
• 1 очко:
  – победитель, но не тот исход (осн. вр. / доп. вр. / пен.)
  – угадано доп. время, но не победитель
  – угадана ничья в осн. время, но не более
• 0 очков — мимо

<b>Лига «Братья» (Ник vs Ваня)</b>
• Ставить можно в любой момент и в любом порядке, ждать брата не нужно
• Одинаковые прогнозы запрещены: в группе прогноз — это счёт, в плей-офф — счёт + исход (например, «2:1 в осн. время» и «2:1 в доп. время» — разные прогнозы, оба разрешены)
• На каждый матч жребий заранее определяет «первого» — при совпадении прогнозов приоритет у него:
  – если второй выбирает занятый прогноз — бот попросит другой
  – если первый выбирает прогноз, который брат уже занял — прогноз брата аннулируется, и тот ставит заново
  – в обоих случаях брату придёт уведомление
• Как только один поставил — второму приходит «Твой ход!»
• Когда поставили оба — обоим раскрываются оба прогноза

<b>Лига «Весенние Зори»</b>
• Все ставят независимо и не видят чужих прогнозов
• Прогнозы раскрываются после начала матча

<b>Таблица</b>
При равенстве очков выше тот, у кого больше угаданных точных счетов.
При полном равенстве — место делится.

<b>Дедлайн</b>
Прогнозы принимаются до момента начала матча. После стартового свистка — закрыто."""


@router.message(Command("rules"))
@router.message(F.text == "📖 Правила")
async def cmd_rules(message: Message):
    await message.answer(RULES_TEXT, parse_mode="HTML")
