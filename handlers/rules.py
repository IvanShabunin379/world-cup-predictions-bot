from aiogram import Router, F
from aiogram.filters import Command
from aiogram.types import Message

router = Router()

RULES_TEXT = """📖 <b>Правила турнира прогнозов ЧМ-2026</b>

<b>Групповой этап</b>
За каждый матч можно получить:
• 3 очка — точный счёт
• 1 очко — угадан исход (победа / ничья), но не счёт
• 0 очков — мимо

<b>Плей-офф</b>
Прогноз включает счёт основного времени и исход:
<b>П1</b> — победа команды 1 в основное время
<b>П2</b> — победа команды 2 в основное время
<b>НП1 / НП2</b> — ничья → победа в доп. времени
<b>НПП1 / НПП2</b> — ничья → победа по пенальти

Очки за плей-офф: от 0 до 4 в зависимости от точности.

<b>Лига «Братья» (Ваня vs Ник)</b>
• На каждый матч заранее определено, кто ставит <b>первым</b>
• Первый ставит → бот уведомляет второго
• Второй <b>не может поставить тот же счёт</b>
• После второго прогноза оба видят оба прогноза

<b>Лига «Весенние Зори»</b>
• Все ставят независимо и не видят чужих прогнозов
• Прогнозы раскрываются после начала матча

<b>Дедлайн</b>
Прогнозы принимаются до момента начала матча. После кикофа — закрыто."""


@router.message(Command("rules"))
@router.message(F.text == "📖 Правила")
async def cmd_rules(message: Message):
    await message.answer(RULES_TEXT, parse_mode="HTML")
